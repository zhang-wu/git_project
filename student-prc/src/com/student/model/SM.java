package com.student.model;

public class SM {
	private int id;
	private string name;
	private string word;
	private int sex;
	private string love;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public string getname() {
		return name;
	}
	public void setname(string name) {
		this.name = name;
	}
	public string getword() {
		return word;
	}
	public void setword(string word) {
		this.word = word;
	}
	public int getSex() {
		return sex;
	}
	public void setSex(int sex) {
		this.sex = sex;
	}
	public string getLove() {
		return love;
	}
	public void setLove(string love) {
		this.love = love;
	}
	
}
